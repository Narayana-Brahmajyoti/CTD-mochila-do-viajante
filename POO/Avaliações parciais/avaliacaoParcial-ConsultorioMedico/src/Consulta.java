public class Consulta {
    private String dataConsulta;
    private String especialidade;
    private int hora;
    private int minuto;


    public Consulta (String dataConsulta, String especialidade, int hora, int minuto){

        this.dataConsulta = dataConsulta;
        this.especialidade = especialidade;
        this.hora = hora;
        this.minuto = minuto;
    }
}
